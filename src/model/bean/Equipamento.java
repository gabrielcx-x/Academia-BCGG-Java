public class Equipamento {
    private String nomeequipamento;
    private String codigoequipamento;
    private String pesosuportado;
    private String grupomuscular;
    private String datamanutencao;

    public String getNomeequipamento() {
        return nomeequipamento;
    }

    public void setNomeequipamento(String nomeequipamento) {
        this.nomeequipamento = nomeequipamento;
    }

    public String getCodigoequipamento() {
        return codigoequipamento;
    }

    public void setCodigoequipamento(String codigoequipamento) {
        this.codigoequipamento = codigoequipamento;
    }

    public String getPesosuportado() {
        return pesosuportado;
    }

    public void setPesosuportado(String pesosuportado) {
        this.pesosuportado = pesosuportado;
    }

    public String getGrupomuscular() {
        return grupomuscular;
    }

    public void setGrupomuscular(String grupomuscular) {
        this.grupomuscular = grupomuscular;
    }

    public String getDatamanutencao() {
        return datamanutencao;
    }

    public void setDatamanutencao(String datamanutencao) {
        this.datamanutencao = datamanutencao;
    }

   
}
